package com.taccotap.taigidictmodel.tailo;

public class TlTaigiMadeWordCode {

    private final String mMadeWordCode;
    private final String mMadeWordReplaceCode;

    public TlTaigiMadeWordCode(String madeWordCode, String madeWordReplaceCode) {
        mMadeWordCode = madeWordCode;
        mMadeWordReplaceCode = madeWordReplaceCode;
    }

    public String getMadeWordCode() {
        return mMadeWordCode;
    }

    public String getMadeWordReplaceCode() {
        return mMadeWordReplaceCode;
    }
}
